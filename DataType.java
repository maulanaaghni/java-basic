public class DataType{

	public static void main(String[] args) {
		
		// Data type in python : 



		// byte -> -128 to 127
		byte thisIsByte = 127;
		System.out.println("this is byte : " + thisIsByte);



		// short -> -32,768 to 32,767
		short thisIsShort = 327;
		System.out.println("this is short : "+ thisIsShort);


		// integer -> -2,147,483,648 to 2,147,483,647
		int thisIsInteger = 30;
		System.out.println("this is integer : "+thisIsInteger);


		// long -> -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		long thisIsLong = 92233720;
		System.out.println("this is long : "+ thisIsLong);



		// float -> 6 to 7 decimal digits
		float thisIsFloat = 30.443f;
		System.out.println("this is float : "+ thisIsFloat);


		// double -> 15 decimal digits
		double thisIsDouble = 4.99943;
		System.out.println("this is double : "+ thisIsDouble);

	}
}